package Concatination;
/**
 * Nan means not a number 
 * @author Ajitabhg
 * created on 06-09-22
 * Modules give us the Remainder
 */
public class ArithmeticConcept {

	public static void main(String[] args) {
		
		System.out.println(10/2);//5
		System.out.println(10/3);//3
		
		System.out.println(".............");
		
		System.out.println(10/3.0);
		
		System.out.println(9.0/0);//infinity
		
		System.out.println(9/0.0);
		
		System.out.println(0/0.0);//NaN
		
		//System.out.println(0/0);// java.lang.ArithmeticException
		
		System.out.println(9 % 3);//0 Modules give us the Remainder
		
		System.out.println(100/0 +"Hello");

	}

}
